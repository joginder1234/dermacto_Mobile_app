class ProductModel{
    constructor(
        productId,
        companyName,
        isPublic,
        productform,
        productImage,
        productName,
        userId
    ){
        this.productId = productId,
        this.companyName = companyName,
        this.isPublic = isPublic,
        this.productform = productform,
        this.productImage = productImage,
        this.productName = productName,
        this.userId = userId
    }
}